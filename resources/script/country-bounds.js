// Returns the country whose bounding box contains [lat, lng].
// When multiple boxes overlap, picks the smallest (most specific) one.
function getCountryFromCoords(lat, lng) {
    let best = null;
    let smallestArea = Infinity;
    for (const [country, [minLat, minLng, maxLat, maxLng]] of Object.entries(COUNTRY_BOUNDS)) {
        if (lat >= minLat && lat <= maxLat && lng >= minLng && lng <= maxLng) {
            const area = (maxLat - minLat) * (maxLng - minLng);
            if (area < smallestArea) {
                smallestArea = area;
                best = country;
            }
        }
    }
    return best;
}
